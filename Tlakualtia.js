function mostrar(){
    document.getElementById('fotitos').style.display = 'flex';
    document.getElementById('boton').style.display = 'none';
    document.getElementById('colegio').style.display = 'none';
    document.getElementById('redes').style.display = 'none';
}
function ocultar(){
    document.getElementById('fotitos').style.display = 'none';
    document.getElementById('boton').style.display = 'block';
    document.getElementById('colegio').style.display = 'block';
    document.getElementById('redes').style.display = 'block';
}